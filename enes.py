class TripletDataset(Dataset):
    def __init__(self, n_samples=20000, seq_len=1000, latent_dim=4):
        self.seq_len = seq_len
        self.latent_dim = latent_dim
        self.data, self.labels = self._generate_sota_data(n_samples)

    def _biochemical_noise(self, size):
        """ SOTA: Non-Gaussian, Heavy-tailed noise (Protein concentrations are rarely Normal) """
        # Mix of Log-Normal and Gumbel to simulate bursty gene expression
        noise = np.exp(np.random.normal(0, 0.3, size)) - 1.0
        return (noise - np.mean(noise)) / (np.std(noise) + 1e-8)

    def _sota_link(self, p, coupling_strength=None):
        p_norm = (p - np.mean(p)) / (np.std(p) + 1e-8)
        strength = coupling_strength if coupling_strength else np.random.uniform(0.8, 2.5)

        mode = np.random.choice(['hill', 'michaelis', 'sigmoid', 'softplus'])

        if mode == 'hill':
            n = np.random.uniform(2, 5)
            # SOTA FIX: Use absolute value for the power, then restore the sign
            p_abs = np.abs(p_norm)
            resp = strength * (p_abs**n) / (1.0**n + p_abs**n + 1e-8)
            resp = resp * np.sign(p_norm) # Restore directional logic

        elif mode == 'michaelis':
            # Michaelis-Menten is naturally robust to negatives if using abs()
            resp = strength * p_norm / (0.5 + np.abs(p_norm))

        elif mode == 'sigmoid':
            # Sigmoid is numerically stable for all real numbers
            resp = strength * (1 / (1 + np.exp(-3.0 * p_norm)))

        else: # softplus
            # Approximates ReLU but differentiable (SOTA for neural SEMs)
            resp = np.log(1 + np.exp(p_norm * strength))

        # Add Heteroscedastic noise (Shot Noise)
        noise = self._biochemical_noise(self.seq_len)
        shot_noise_scale = 0.1 * np.abs(resp)
        return resp + (noise * (1.0 + shot_noise_scale))

    def _generate_sota_data(self, n_samples):
        D, L = [], []
        # Increase variety of per-run variations
        iters = n_samples // 120

        for _ in range(iters):
            # U is a global latent context (e.g. cell size/state) affecting all downstream
            u_global = self._biochemical_noise(self.seq_len)

            s = lambda: self._biochemical_noise(self.seq_len)

            # --- CLASS 0: Independent (Baseline) ---
            D.append(np.stack([s(), s(), s()])); L.append(0)

            # --- CLASS 1: Chain (X -> Y -> Z) ---
            # SOTA: We use the latent U to create "Partial Confounding" -
            # This forces the model to find the direct path through the shared noise.
            x1 = s() + 0.2*u_global
            y1 = self._sota_link(x1) + 0.2*u_global
            z1 = self._sota_link(y1) + 0.2*u_global
            D.append(np.stack([x1, y1, z1])); L.append(1)

            # --- CLASS 2: Fork (X <- Y -> Z) ---
            y2 = s() + 0.5*u_global
            x2 = self._sota_link(y2)
            z2 = self._sota_link(y2)
            D.append(np.stack([x2, y2, z2])); L.append(2)

            # --- CLASS 3: Collider (X -> Y <- Z) ---
            # High Signal-to-Noise ratio for the Y integration
            x3, z3 = s(), s()
            y3 = self._sota_link(x3, 1.2) + self._sota_link(z3, 1.2)
            D.append(np.stack([x3, y3, z3])); L.append(3)

            # --- CLASS 4: SOTA Latent Confounder (X <- U -> Z) ---
            # No direct edge between X and Z, but they share a powerful hidden cause.
            # This is the 'hallucination trap'.
            u_hidden = s()
            x4 = self._sota_link(u_hidden)
            z4 = self._sota_link(u_hidden)
            y4_rand = s()
            D.append(np.stack([x4, y4_rand, z4])); L.append(4)

            # --- CLASS 5: Selection Bias (M-Structure) ---
            # X and Z are independent, but both influence a HIDDEN variable.
            # This tests if the model mistakenly orients edges due to selection.
           #nd(np.stack([x5, y5_rand, z5])); L.append(5)

        return torch.tensor(np.array(D), dtype=torch.float32), torch.tensor(L, dtype=torch.long)

    def __len__(self): return len(self.labels)
    def __getitem__(self, i):
        # SOTA normalization: Instance Norm (Z-score per triplet)
        x = self.data[i]
        x = (x - x.mean(dim=-1, keepdim=True)) / (x.std(dim=-1, keepdim=True) + 1e-8)
        return x, self.labels[i]
