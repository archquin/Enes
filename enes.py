class CausalDualExpert(nn.Module):
    """
    Combines a Statistical (Linear) view and a Structural (Non-Linear) view
    to determine the score for a specific causal class.
    """
    def __init__(self, cnn_dim, stat_dim, hidden_dim):
        super().__init__()

        # Expert A: The Statistician (Sees Pearson, Skew, Kurtosis)
        self.linear_expert = nn.Sequential(
            nn.Linear(stat_dim, hidden_dim),
            nn.BatchNorm1d(hidden_dim),
            nn.ReLU(),
            nn.Linear(hidden_dim, hidden_dim // 2),
            nn.ReLU(),
            nn.Linear(hidden_dim // 2, 1)
        )

        # Expert B: The Visionary (Sees CNN Motif Features)
        self.nonlinear_expert = nn.Sequential(
            nn.Linear(cnn_dim, hidden_dim),
            nn.BatchNorm1d(hidden_dim),
            nn.LeakyReLU(0.2),
            nn.Linear(hidden_dim, hidden_dim // 2),
            nn.LeakyReLU(0.2),
            nn.Linear(hidden_dim // 2, 1)
        )

        # Learnable Gate: Balances the importance of Linear vs Non-Linear for this class
        self.consensus_weight = nn.Parameter(torch.ones(1) * 0.5)

    def forward(self, cnn_feats, stat_feats):
        l_logit = self.linear_expert(stat_feats)
        nl_logit = self.nonlinear_expert(cnn_feats)

        # Weight the two opinions
        w = torch.sigmoid(self.consensus_weight)
        return (w * l_logit) + ((1 - w) * nl_logit)
class NeuralTripletProber(nn.Module):
    def __init__(self, h=128):
        super().__init__()

        # --- 1. SHARED FEATURE EXTRACTION ---
        # Scans the 3-node sequence for non-linear "textures"
        self.motif_scanner = nn.Sequential(
            nn.Conv1d(3, h, 3, padding=1), nn.BatchNorm1d(h), nn.LeakyReLU(0.2),
            nn.Conv1d(h, h, 3, padding=2, dilation=2), nn.BatchNorm1d(h), nn.LeakyReLU(0.2),
            nn.Conv1d(h, h, 1), nn.BatchNorm1d(h), nn.AdaptiveAvgPool1d(1)
        )

        # --- 2. THE CAUSAL COMMITTEES ---
        # Each class has its own Linear/Non-Linear dual expert
        # stat_dim = 9 (3 Corrs, 3 Skew, 3 Kurtosis)
        expert_cfg = (h, 9, h // 2)

        self.class_0 = CausalDualExpert(*expert_cfg) # Independence
        self.class_1 = CausalDualExpert(*expert_cfg) # Chain
        self.class_2 = CausalDualExpert(*expert_cfg) # Fork
        self.class_3 = CausalDualExpert(*expert_cfg) # Collider
        self.class_4 = CausalDualExpert(*expert_cfg) # Asymmetric/Invalid

    # --- THE SPINE (DAG & Pearson Constraints) ---
    def get_triplet_adjacency(self, logits):
        probs = torch.softmax(logits, dim=-1)
        W = torch.zeros((logits.shape[0], 3, 3), device=logits.device)
        # Mapping logits to a 3x3 adjacency matrix
        W[:, 0, 1] = probs[:, 1] + probs[:, 3] # x->y in Chain(1) or Collider(3)
        W[:, 1, 2] = probs[:, 1] + probs[:, 2] # y->z in Chain(1) or Fork(2)
        W[:, 1, 0] = probs[:, 2] + probs[:, 4] # y->x in Fork(2) or Asym(4)
        W[:, 2, 1] = probs[:, 3] + probs[:, 4] # z->y in Collider(3) or Asym(4)
        return W

    def compute_topological_penalty(self, W):
        d = 3
        W_sq = W * W
        I = torch.eye(d, device=W.device).unsqueeze(0)
        M = I + W_sq / d
        E = torch.matrix_power(M, d) # Trace(E) - d = 0 implies acyclicity
        return (torch.diagonal(E, dim1=-2, dim2=-1).sum(-1) - d).mean()

    def compute_pearson_penalty(self, logits, corrs):
        probs = torch.softmax(logits, dim=-1)
        prob_exists = 1.0 - probs[:, 0] # 1 - P(Independence)
        avg_corr = torch.abs(corrs).mean(dim=-1)
        return (prob_exists * (1.0 - avg_corr)).mean()

    # --- FORWARD PASS ---
    def forward(self, x):
        # 1. Structural Branch (CNN)
        cnn_feats = self.motif_scanner(x).squeeze(-1)

        # 2. Statistical Branch (Math)
        m = x.mean(dim=-1, keepdim=True)
        s = x.std(dim=-1, unbiased=False, keepdim=True) + 1e-8
        z = (x - m) / s
        skew = (z ** 3).mean(dim=-1)
        kurt = (z ** 4).mean(dim=-1) - 3.0

        # Calculate Pearson Matrix
        x_n = x - m
        cov = torch.matmul(x_n, x_n.transpose(1, 2)) / (x.shape[-1] - 1)
        corr_mat = cov / (torch.matmul(s, s.transpose(1, 2)) + 1e-8)
        corrs = torch.stack([corr_mat[:,0,1], corr_mat[:,0,2], corr_mat[:,1,2]], dim=-1)

        stat_feats = torch.cat([skew, kurt, corrs], dim=-1)

        # 3. Decision Layer (Ensemble Voting)
        logits = torch.cat([
            self.class_0(cnn_feats, stat_feats),
            self.class_1(cnn_feats, stat_feats),
            self.class_2(cnn_feats, stat_feats),
            self.class_3(cnn_feats, stat_feats),
            self.class_4(cnn_feats, stat_feats)
        ], dim=-1)

        # 4. Multi-Objective Penalties
        W = self.get_triplet_adjacency(logits)
        topo_p = self.compute_topological_penalty(W)
        pearson_p = self.compute_pearson_penalty(logits, corrs)

        total_p = (1.0 * topo_p) + (1.0 * pearson_p)

        return logits, total_p











