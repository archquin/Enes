def get_partial_correlation(X, i, j, k):
    """Calculates partial correlation of i and j given k."""
    rho = np.corrcoef(X[:, [i, j, k]].T)
    r_ij = rho[0, 1]
    r_ik = rho[0, 2]
    r_jk = rho[1, 2]

    numerator = r_ij - (r_ik * r_jk)
    denominator = np.sqrt((1 - r_ik**2) * (1 - r_jk**2)) + 1e-8
    return numerator / denominator

def post_prune_graph(W_pred, X_obs, threshold=0.1):
    """
    Identifies and removes indirect edges in the predicted graph.
    If X -> Y exists, but partial_corr(X, Y | Z) is low,
    it implies the link is likely X -> Z -> Y or spurious.
    """
    W_pruned = W_pred.copy()
    d = W_pruned.shape[0]

    # Iterate over all detected edges
    edges = np.argwhere(W_pruned > 0)
    for u, v in edges:
        # Check every other node k as a potential mediator
        for k in range(d):
            if k == u or k == v:
                continue

            # We only test k if it has some relationship with the pair
            p_corr = abs(get_partial_correlation(X_obs, u, v, k))

            # If correlation drops significantly, prune the edge
            if p_corr < threshold:
                W_pruned[u, v] = 0
                break # Move to next edge

    return W_pruned



