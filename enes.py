def get_neural_asymmetry_score(i, j, X_working, model, device):
    d = X_working.shape[1]
    votes_ij, votes_ji = 0.0, 0.0
    model.eval() # Ensure dropout/batchnorm are in eval mode
    with torch.no_grad():
        for k in range(d):
            if k == i or k == j: continue

            # Helper to get probs from the new dual-return forward
            def get_probs(nodes):
                trip = torch.tensor(X_working[:, nodes].T, dtype=torch.float32).unsqueeze(0).to(device)
                trip = (trip - trip.mean(-1, keepdim=True)) / (trip.std(-1, keepdim=True) + 1e-8)
                logits, _ = model(trip) # Unpack and ignore the penalty (_)
                return F.softmax(logits, dim=-1).squeeze(0)

            probs = get_probs([i, j, k])
            probs_rev = get_probs([j, i, k])

            # Logic: Chain/Collider indicators minus the "Independence/Noise" class (4)
            votes_ij += (probs[1].item() + probs[3].item() + probs_rev[2].item()) - (probs[4].item() + probs_rev[4].item())
            votes_ji += (probs[2].item() + probs_rev[1].item() + probs_rev[3].item()) - (probs[4].item() + probs_rev[4].item())

    return -votes_ij, -votes_ji

def find_triplets(X_obs, d):
    v_structs = []
    for y in range(d):
        for x in range(d):
            for z in range(x + 1, d):
                if x == y or y == z: continue
                r_xy = abs(np.corrcoef(X_obs[:, x], X_obs[:, y])[0, 1])
                r_yz = abs(np.corrcoef(X_obs[:, y], X_obs[:, z])[0, 1])
                r_xz = abs(np.corrcoef(X_obs[:, x], X_obs[:, z])[0, 1])
                denom = np.sqrt((1 - r_xy**2) * (1 - r_yz**2)) + 1e-8
                pcorr_xz_y = (r_xz - r_xy * r_yz) / denom
                if r_xz > 0.15 and r_yz > 0.15 and r_xy < 0.1 and abs(pcorr_xz_y) > 0.15:
                    v_structs.append((abs(pcorr_xz_y), x, z, y))
    return v_structs

def layered_causal_discovery_neural(X_obs, d, model, device, max_edges=17):
    W_pred = np.zeros((d, d))
    X_working = X_obs.copy()
    edges_count = 0
    v_structs = find_triplets(X_obs, d)
    v_structs.sort(key=lambda x: x[0], reverse=True)
    for _, u, v, w in v_structs:
        if edges_count < max_edges - 1:
            if W_pred[v, u] == 0 and W_pred[v, w] == 0:
                W_pred[u, v], W_pred[w, v] = 1, 1
                edges_count += 2
    model.eval()
    while edges_count < max_edges:
        best_pair, max_sim = None, -np.inf
        for i in range(d):
            for j in range(d):
                if i == j or W_pred[i, j] == 1 or W_pred[j, i] == 1: continue
                sim = abs(np.corrcoef(X_working[:, i], X_working[:, j])[0, 1])
                if sim > max_sim and sim > 0.08:
                    s_ij, s_ji = get_neural_asymmetry_score(i, j, X_working, model, device)
                    if s_ij < s_ji: max_sim, best_pair = sim, (i, j)
        if not best_pair: break
        u, v = best_pair
        W_pred[u, v] = 1
        edges_count += 1
        slope, intercept, _, _, _ = stats.linregress(X_working[:, u], X_working[:, v])
        X_working[:, v] -= (slope * X_working[:, u] + intercept)
        X_working[:, v] = (X_working[:, v] - np.mean(X_working[:, v])) / (np.std(X_working[:, v]) + 1e-8)

        W_refined = post_prune_graph(W_pred, X_obs, threshold=0.12)

    return W_refined

def fast_pearson_discovery(X_obs, d, max_edges=17):
    W_pred = np.zeros((d, d))
    corr_matrix = np.abs(np.corrcoef(X_obs.T))
    np.fill_diagonal(corr_matrix, 0)
    flat_indices = np.argsort(corr_matrix, axis=None)[::-1]
    for idx in flat_indices[:max_edges]:
        u, v = np.unravel_index(idx, corr_matrix.shape)
        if W_pred[v, u] == 0: W_pred[u, v] = 1
    return W_pred

