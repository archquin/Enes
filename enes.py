def calculate_metrics(W_pred, W_true):
    TP = np.sum((W_pred != 0) & (W_pred == W_true))
    p = TP / np.sum(W_pred != 0) if np.sum(W_pred != 0) > 0 else 0
    r = TP / np.sum(W_true != 0) if np.sum(W_true != 0) > 0 else 0
    f1 = 2 * p * r / (p + r) if (p + r) > 0 else 0
    shd = np.sum(W_pred != W_true)
    return p, r, f1, shd

def generate_sachs_obs(df, n_obs=2000):
    d = 11
    sampled = df.sample(n=n_obs, replace=True).values
    X = (sampled - np.mean(sampled, axis=0)) / (np.std(sampled, axis=0) + 1e-8)
    W_true = np.zeros((d, d))
    edges = [(8, 7), (8, 10), (8, 9), (8, 0), (8, 1), (7, 0), (7, 1), (7, 5), (7, 6), (7, 9), (7, 10), (0, 1), (1, 5), (2, 3), (4, 3), (4, 2), (4, 6)]
    for i, j in edges: W_true[i, j] = 1
    return X, W_true

